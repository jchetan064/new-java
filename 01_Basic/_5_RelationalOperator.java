/**
 * _5_RelationalOperator
 */
public class _5_RelationalOperator {

    public static void main(String[] args) {
        

        // These Operators are gives the o/p in true or false format 

        int a = 10;
        int b = 12;

        System.out.println( a > b );
        System.out.println( a < b );
        System.out.println( a == b );
        System.out.println( a >= b );
        System.out.println( a <= b );
        System.out.println( a != b );
    }
}
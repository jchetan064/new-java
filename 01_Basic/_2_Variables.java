class _2_Variables {

    public static void main(String[] args) {

        // integer  -> byte(1) , short(2) , int(4) , long(8)
       
            
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println( c );



        // double  -> double (8), float(4) 
        
        double a1 = 8.9403934;
        float b1 = 898.44353f;

        System.out.println(a1);
        System.out.println(b1);
        
        // Char  -> char , string

        char c1 = 'q';
        System.out.println(c1); 
        
        // boolean 
        boolean d = true;
        System.out.println(d);
    }
}

public class _4_Operators {

    public static void main(String[] args) {

        int a = 12;
        int b = 23;

        System.out.println( a + b );
        System.out.println( a - b );
        System.out.println( a * b );
        System.out.println( a / b );
        System.out.println( a % b );


        // a++ -> Post Increment
        // --a -> Pre Increment

        System.out.println( a++ );
        System.out.println( a-- );
        System.out.println( a += 20 );
        System.out.println( a -= 2 );
        System.out.println( a *= 2 );
        System.out.println( a /= 12 );

    }
}